macro_rules! enum_with_constants {
    ($name:ident { $($constant:ident = $value:expr),* }) => {
        #[derive(Debug, PartialEq, Eq)]
        enum $name {
            $($constant),*
        }

        impl $name {
            fn from_constant(value: usize) -> Option<Self> {
                match value {
                    $($value => Some(Self::$constant),)*
                    _ => None
                }
            }

            fn to_constant(&self) -> usize {
                match self {
                    $(Self::$constant => $value,)*
                }
            }
        }
    };
}

enum_with_constants!(MyEnum {
    A = 1,
    B = 2
});
